<p>You have the right to access the personal information the Group collects and holds about you. If at any time you would like to access your details or you would you like more information on our approach to privacy, please tell us.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>

<p><strong>Changing information</strong></p>

<p>If you want to change your personal details held by the Group, where you are not able to edit these directly, please let us know in writing.</p>

<p><strong>Changes to the Privacy Policy</strong></p>

<p>The Privacy Policy may be updated at any time. If the changes will impact your privacy, we will notify you of the changes.</p>

