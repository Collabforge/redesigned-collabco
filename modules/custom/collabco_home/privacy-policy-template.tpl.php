<p><strong>What kinds of personal information does the Group collect?</strong></p>

<p>Generally, the Group will collect personal information, such as your name and contact details including organisational address, e-mail address and any other information, which is necessary to provide services to you.</p>

<p><strong>Collection of personal information</strong></p>

<p>Generally, the Group collects your personal information directly from you by requesting that you complete an application form through the registration process.</p>

<p><strong>Why does the Group need personal information?</strong></p>

<p>The Group collects this information for the purposes of:</p>

<p>●&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Enabling you to enter into discussions in members’ sections of the site</p>

<p>●&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Enabling you to contact other site members</p>

<p>●&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In order for you to be identified and contacted by other site members</p>

<p>●&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In order for us to understand your interests and invite you to be involved in relevant events</p>

<p>●&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In order for us to allow you into collaborative working spaces</p>

<p>●&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In order to understand the demographics of parties interested in innovation collectively</p>

